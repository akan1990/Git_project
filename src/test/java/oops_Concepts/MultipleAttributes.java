package oops_Concepts;

public class MultipleAttributes {
	String fname = "John";
	String lname = "Singh";
	int age = 25;

	public static void main(String[] args) {
		MultipleAttributes p1 = new MultipleAttributes();
		System.out.println("FirstName:  " + p1.fname + "  " + "LastName:  " + p1.lname);
		System.out.println("Age:  " + p1.age);

	}

}
